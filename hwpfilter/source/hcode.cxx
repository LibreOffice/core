/* Convert a combination of a color index value and a shade value to the color value of LibreOffice */
char *hcolor2str(uchar color, uchar shade, char *buf, bool bIsChar)
{
    unsigned short red,green,blue;

    switch( static_cast<int>(color) )
    {
        case 0 :                                  // black
            red =  0xff * (100 - shade ) /100;
            green =  0xff * (100 - shade ) /100;
            blue =  0xff * (100 - shade ) /100;
            break;
        case 1:                                   // blue
            red =  0xff * (100 - shade ) /100;
            green =  0xff * (100 - shade ) /100;
            blue = 0xff;
            break;
        case 2:                                   // green
            red =  0xff * (100 - shade ) /100;
                if( bIsChar )
                     green = 0x80;
                else
                     green = 0xff;
            blue =  0xff * (100 - shade ) /100;
            break;
        case 3:                                   // cyan
            red =  0xff * (100 - shade ) /100;
            green = 0xff;
            blue = 0xff;
            break;
        case 4:                                   // red
            red = 0xff;
            green =  0xff * (100 - shade ) /100;
            blue =  0xff * (100 - shade ) /100;
            break;
        case 5:                                   // magenta
            red = 0xff;
            green =  0xff * (100 - shade ) /100;
            blue = 0xff;
            break;
        case 6:                                   //yellow
            red = 0xff;
            green = 0xff;
            blue =  0xff * (100 - shade ) /100;
            break;
        case 7:                                   //white
        default:
            red = 0xff;
            green = 0xff;
            blue = 0xff;
            break;
    }

    sprintf(buf,"#%02x%02x%02x", red, green, blue);
    return buf;
}


