    // Daher muss der Text getrennt geparst werden (in FillTxtNode())
    if( pSet ) pSet->Put( aAttr );
    else pNd->Insert( aAttr, nBgn, nEnd, SETATTR_NOTXTATRCHR );
    return aAttr.Which();
